      <p>They said "you'll hear by Friday." It's Tuesday. Silence usually means process delays, not rejection: an interviewer on leave, a budget signature pending, another candidate's schedule. Nudge once, lightly:</p>
      <blockquote>
        <p>Subject: Re: [Role] — checking in</p>
        <p>Hi [Name],</p>
